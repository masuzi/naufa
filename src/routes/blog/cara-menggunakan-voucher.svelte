  <p class="font-bold mb-3">Cara Menggunakan atau Simantep Wisesa WiFi.</p>
  <p>
    Untuk melakukan login Simantep Wisesa, kamu dapat mengikuti langkah-langkah berikut.
  </p>
  <ul>
    <li>
      - Pastikan PC/laptop/smartphone kamu berada di wilayah jangkauan sinyal Wifi
      Simantep Wisesa
    </li>
    <li>
      - Di menu WiFi kamu akan muncul Access Point SImantep Wisesa. Pilihlah jaringan
      tersebut.
    </li>
    <li>
      - Buka browser Chrome atau Mozilla di PC/laptop/smartphone kamu dan halaman utama
      akan segera diarahkan pada halaman Log In Simantep Wisesa
      <img
        alt="Beli Voucher"
        src="https://res.cloudinary.com/masuzi/image/upload/v1651035824/wifi/clxpfimhmx5ytq4xqaoe.webp"
      />
    </li>
    <li>
      - Masukkan kode dan password yang telah diberikan pada voucher Simantep Wisesa
    </li>
    <li>
      - Setelah selesai, akun Simantep kamu sudah aktif dan jaringan internetnya sudah
      dapat diakses.
    </li>
  </ul>
